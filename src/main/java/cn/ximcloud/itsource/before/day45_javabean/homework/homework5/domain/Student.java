public class Student {
    private Integer id;
    private Integer age;
    private Boolean sex;


    public Student() {
    }

    public Student(Integer id, Integer age, Boolean sex) {
        this.id = id;
        this.age = age;
        this.sex = sex;
    }

    public Student(Integer age, Boolean sex) {
        this.age = age;
        this.sex = sex;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", age=" + age +
                ", sex=" + sex +
                '}';
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Boolean getSex() {
        return sex;
    }

    public void setSex(Boolean sex) {
        this.sex = sex;
    }
}
