public class StudentList {
    private Integer id;
    private String name;


    public StudentList() {
    }

    public StudentList(Integer id) {
        this.id = id;
    }

    public StudentList(String name) {
        this.name = name;
    }

    public StudentList(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public String toString() {
        return "ListImpl{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
