public class Student {
    public String name;
    public int age;
    private Student() {

    }

    public Student(String name, int age) {
        this.name = name;
        this.age = age;

    }

    private Student(String name) {
    }

    @Override
    public String toString() {
        return "Student{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
