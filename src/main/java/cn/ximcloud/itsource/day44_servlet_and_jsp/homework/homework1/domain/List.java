public class List {
    //    邮件列表的分类
    private Integer id;
    //    邮件列表的名称
    private String name;
    //    邮件列表的类型
    private String type;

    /**
     * 必须有一个空的构造方法让反射访问
     */
    public List() {
    }

    public List(String name, String type) {
        this.name = name;
        this.type = type;
    }

    @Override
    public String toString() {
        return "StudentList{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", type='" + type + '\'' +
                '}';
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
